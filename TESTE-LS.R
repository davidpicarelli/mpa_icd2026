serie_precos <- c("PETR4.SA", "MGLU3.SA", "ITUB4.SA") %>%
  # importa as séries de preços das ações: PETR4, MGLU3 e ITUB4
  tq_get(from = "2024-01-01") %>%
  # seleciona as variáveis relevantes
  select(symbol, date, adjusted) %>%
  # converte do formato longo para o formato largo
  pivot_wider(names_from = symbol, values_from = adjusted) %>%
  # renomeia as variáveis
  rename(
    dia = date,
    petr4 = PETR4.SA,
    mglu3 = MGLU3.SA,
    itub4 = ITUB4.SA
  )

